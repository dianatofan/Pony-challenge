import { BASE_URL } from "./CONSTANTS";

export const post = async (url, payload) =>
  await fetch(url, {
    method: "POST",
    headers: {
      Accept: "application/json",
      "Content-Type": "application/json",
    },
    body: JSON.stringify(payload),
  });

